package javaapplication18;

/**
 *
 * @author ASUS
 */
public class Employee {
    
    private String name;
    private String area;
    private String company;

    public Employee(String name, String area, String company) {
        this.name = name;
        this.area = area;
        this.company = company;
    }

    public String getName() {
        return name;
    }

    public String getArea() {
        return area;
    }

    public String getCompany() {
        return company;
    }
    
    
    
    
}
